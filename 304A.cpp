void compute(){
    int n;
    cin>>n;
    int ct = 0;
    for(int i = n;i>=5;i--){
        int c_sq = i*i;
        int range = sqrt(c_sq/2);
        for(int j = 3;j<=range;j++){
            int a_sq = j*j;
            int b = sqrt(c_sq - a_sq);
            if(b*b == c_sq - a_sq)++ct;
        }
    }
    cout<<ct;
}
